    void saveAlldata(){
        try {
            try (FileWriter filewriter = new FileWriter("pdata.txt")) {
                for (patient allpatient : allpatients) {
                    filewriter.write(allpatient.getId() + ";" + allpatient.getName() + ";" + allpatient.getAge() + ";" + allpatient.getGender() + ";" + allpatient.getAddress() + ";" + allpatient.getContact()+"\n");
                }
            }
            
            
        } catch (IOException ex) {
            Logger.getLogger(Patients.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
    
    
    
    boolean cheackIDC(){
        for (patient allpatient : allpatients) {
            if (Id.getText().equals(allpatient.getId() + "") || Contact.getText().equals(allpatient.getContact() + "")) {
                return true;
            }
        }
        return false;
    }
       
    
