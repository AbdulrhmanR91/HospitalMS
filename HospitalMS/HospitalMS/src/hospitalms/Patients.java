    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(Id.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter Valid Id for search!");
            
        }
        else{
            patient patient=null;
            for(int i=0;i<allpatients.size();i++){
                 if(Id.getText().equals(allpatients.get(i).getId()+"")){
                     patient=allpatients.get(i);
                     break;
                 }
                 
            }
            if(patient==null){
                JOptionPane.showMessageDialog(null, "Please Enter Valid Id for search!");
            }
            else{
                JOptionPane.showMessageDialog(null, "patient Info\n Name = "+patient.getName()+"\n Contact = "+patient.getContact()+"\nAddress = "+patient.getAddress());
                
            }
        }

        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(Id.getText().equals("")||Contact.getText().equals("")||Address.getText().equals("")||Gender.getText().equals("")||Name.getText().equals("")||Age.getText().equals(""))
            {JOptionPane.showMessageDialog(null, "All fields Requierd !");
        }
        else if(cheackIDC()){
            JOptionPane.showMessageDialog(null, "ID or Contact Already Exists");            
        }
        else{
            try{
                patient patient = new patient();
                patient.setId(Integer.parseInt(Id.getText()));
                patient.setName((Name.getText()));
                patient.setAge(Integer.parseInt(Age.getText()));
                patient.setGender(Gender.getText());   
                patient.setAddress(Address.getText());
                patient.setContact(Contact.getText());
                allpatients.add(patient);
                
                Id.setText("");
                Name.setText("");
                Address.setText("");
                Contact.setText("");
                Age.setText("");
                Gender.setText("");
                writeAllData();
                saveAlldata();
                
                
            }
            catch(Exception e){
            JOptionPane.showMessageDialog(null, "ID or Contact Must be Integer");            
                
            }
        
            
        }
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

